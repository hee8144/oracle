SELECT DNAME,평균급여
FROM(
SELECT ROWNUM RN,E.*
FROM(
SELECT DNAME ,ROUND(AVG(SAL),0) 평균급여
FROM EMP E
INNER JOIN DEPT D ON E.DEPTNO = D.DEPTNO
GROUP BY DNAME
ORDER BY ROUND(AVG(SAL),0) DESC
) E
)
