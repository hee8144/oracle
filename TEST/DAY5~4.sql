--학생테이블에서 키가 큰 상위 5명만 출력

SELECT * FROM STUDENT S
ORDER BY STU_HEIGHT DESC;

SELECT ROWNUM ,S.*
FROM(
SELECT * FROM STUDENT
WHERE STU_HEIGHT IS NOT NULL
ORDER BY STU_HEIGHT DESC) S
WHERE ROWNUM BETWEEN 1 AND 5;

