-- 3. 본인 학과에서 본인보다 몸무게가 큰 학생의 수를 출력하시오. ( 학번, 이름, 학과, 큰 학생 수 출력 )
-- (STUDENT)

SELECT * FROM STUDENT;

SELECT COUNT(S2.STU_DEPT),S1.STU_NO,S1.STU_NAME,S1.STU_DEPT
FROM STUDENT S1
LEFT JOIN  STUDENT S2 ON S1.STU_HEIGHT < S2.STU_HEIGHT AND S1.STU_DEPT = S2.STU_DEPT
GROUP BY S1.STU_NAME ,S1.STU_NO,S1.STU_DEPT
;




