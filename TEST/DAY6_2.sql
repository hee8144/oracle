--	(PROFESSOR, DEPARTMENT)
SELECT * FROM PROFESSOR;
SELECT * FROM DEPARTMENT;

SELECT NAME ,DNAME , POSITION,PAY
FROM PROFESSOR P
    INNER JOIN DEPARTMENT D ON P.DEPTNO = D.DEPTNO
    WHERE PAY =(   
        SELECT MAX(PAY) 
        FROM DEPARTMENT D1 
        INNER JOIN DEPARTMENT D2 ON D1.PART = D2.DEPTNO
        INNER JOIN DEPARTMENT D3 ON D2.PART = D3.DEPTNO
        INNER JOIN PROFESSOR P ON D1.DEPTNO = P.DEPTNO
        WHERE D3.DNAME = '공과대학'
        GROUP BY D3.DEPTNO
) OR PAY=(
    SELECT MIN(PAY) 
    FROM DEPARTMENT D1 
    INNER JOIN DEPARTMENT D2 ON D1.PART = D2.DEPTNO
    INNER JOIN DEPARTMENT D3 ON D2.PART = D3.DEPTNO
    INNER JOIN PROFESSOR P ON D1.DEPTNO = P.DEPTNO
    WHERE D3.DNAME = '공과대학'
    GROUP BY D3.DEPTNO
)
;



